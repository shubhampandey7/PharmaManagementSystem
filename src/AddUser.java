    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userRole =(String)comboUserRole.getSelectedItem();
        String name =txtName.getText();
        SimpleDateFormat dFormat=new SimpleDateFormat("dd-MM-yyyy");
        Date date=dateDOB.getDate();
        String dob="";
        if(date!=null){
            dob=dFormat.format(dateDOB.getDate());
        }
        
        String mobileNumber =txtMobile.getText();
        String email=txtEmail.getText();
        String password=txtPsd.getText();
        String address=txtAddress.getText();
        String userName=txtUN.getText();
        if(name.equals(""))
            JOptionPane.showMessageDialog(null, "Name field is required.");
        else if(dob.equals(""))
            JOptionPane.showMessageDialog(null, "Date of Birth field is required.");
        else if(mobileNumber.equals(""))
            JOptionPane.showMessageDialog(null, "mobile field is required.");
        else if(!mobileNumber.matches(mobilePattern)|| mobileNumber.length()!=10)
            JOptionPane.showMessageDialog(null, "Invalid mobile number.");
        else if(email.equals(""))
            JOptionPane.showMessageDialog(null, "Email field is required.");
        else if(!email.matches(emailPattern))
            JOptionPane.showMessageDialog(null, "Invalid email address.");
        else if(password.equals(""))
            JOptionPane.showMessageDialog(null, "Password field is required.");
        else if(password.length()<8)
            JOptionPane.showMessageDialog(null, "Password length invalid.");
        else if(address.equals(""))
            JOptionPane.showMessageDialog(null, "Address field is required.");
        else if(checkUsername==1)
            JOptionPane.showMessageDialog(null, "User name already exist.");
        else if(userName.equals(""))
            JOptionPane.showMessageDialog(null, "User Name field is required.");
        else{
            try{
                Connection con=ConnectionProvider.getcon();
                PreparedStatement ps = con.prepareStatement("insert into appuser(userRole,name,dob,mobileNumber,email,username,password,address)values(?,?,?,?,?,?,?,?)");
                ps.setString(1, userRole);
                ps.setString(2, name);
                ps.setString(3, dob);
                ps.setString(4, mobileNumber);
                ps.setString(5, email);
                ps.setString(6, userName);
                ps.setString(7, password);
                ps.setString(8, address);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "User added successfully");
                setVisible(false);
                new AddUser().setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

