    private void txtUNKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUNKeyReleased
        // TODO add your handling code here:
        String username=txtUN.getText();
        if(!username.equals("")){
            iconLable1.setVisible(true);
            iconLable1.setIcon(new ImageIcon("src\\images\\yes.png"));
            iconLable1.setText("");
            checkUsername=0;
            try{
                Connection con=ConnectionProvider.getcon();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select * from appuser where username ='"+username+"'");
                while(rs.next()){
                    checkUsername=1;
                    iconLable1.setIcon(new ImageIcon("src\\images\\no.png"));
                }
                
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
        else
            iconLable1.setVisible(true);
            
    }//GEN-LAST:event_txtUNKeyReleased

